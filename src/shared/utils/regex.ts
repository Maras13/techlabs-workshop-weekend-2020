// Checks if at least one lower case letter exists
export const lowercase = /(?=.*[a-z])/;

// Checks if at least one upper case letter exists
export const uppercase = /(?=.*[A-Z])/;

// Checks if at least one digit exists
export const digit = /(?=.*\d)/;

// Checks if at least one non-word exists
export const specialSymbol = /(?=.*\W)/;
